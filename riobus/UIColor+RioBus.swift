import UIKit

extension UIColor {
    class func appOrangeColor() -> UIColor {
        return UIColor(red: 243.0/255.0, green: 102.0/255.0, blue: 32.0/255.0, alpha: 1.0)
    }
    
    class func appLightBlueColor() -> UIColor {
        return UIColor(red: 112.0/255.0, green: 180.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
    
    class func appDarkBlueColor() -> UIColor {
        return UIColor(red: 34.0/255.0, green: 127.0/255.0, blue: 238.0/255.0, alpha: 1.0)
    }
}
